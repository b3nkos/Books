fun main() {
    // Using val for constants
    val firsName = "Cristian"
    val lasName = "Martinez"

    // Using var for values that can change
    var age = 33

    println("My name is $firsName $lasName and I am $age years old.")

    // Increment age
    age += 1
    println("Next year, I will be $age years old!")
}